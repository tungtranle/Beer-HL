DO $$
DECLARE
  cnt_customers INTEGER;
  cnt_products INTEGER;
  cnt_vehicles INTEGER;
  cnt_drivers INTEGER;
  cnt_orders INTEGER;
BEGIN
  SELECT COUNT(*) INTO cnt_customers FROM customers;
  SELECT COUNT(*) INTO cnt_products FROM products;
  SELECT COUNT(*) INTO cnt_vehicles FROM vehicles;
  SELECT COUNT(*) INTO cnt_drivers FROM drivers;
  SELECT COUNT(*) INTO cnt_orders FROM sales_orders;

  RAISE NOTICE '========================================';
  RAISE NOTICE '  ĐÃ NẠP DATA SẢN XUẤT THÀNH CÔNG';
  RAISE NOTICE '  Khách hàng (NPP): %', cnt_customers;
  RAISE NOTICE '  Sản phẩm: %', cnt_products;
  RAISE NOTICE '  Xe: %', cnt_vehicles;
  RAISE NOTICE '  Tài xế: %', cnt_drivers;
  RAISE NOTICE '  Đơn hàng mẫu: %', cnt_orders;
  RAISE NOTICE '========================================';
END $$;
