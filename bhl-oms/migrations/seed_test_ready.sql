-- CẬP NHẬT SỐ ĐIỆN THOẠI CHO CUSTOMERS (để Zalo confirmation hoạt động)
-- ============================================================
WITH numbered AS (
  SELECT id, ROW_NUMBER() OVER (ORDER BY code) AS rn FROM customers
)
UPDATE customers SET phone = '09' || LPAD(numbered.rn::text, 8, '0')
FROM numbered WHERE customers.id = numbered.id AND (customers.phone IS NULL OR customers.phone = '');

