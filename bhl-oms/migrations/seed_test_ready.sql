-- KIỂM TRA KẾT QUẢ
-- ============================================================
SELECT '--- KẾT QUẢ SEED ---' AS info;
SELECT 'products' AS item, count(*) AS "số lượng" FROM products
UNION ALL SELECT 'customers', count(*) FROM customers
UNION ALL SELECT 'lots', count(*) FROM lots
UNION ALL SELECT 'stock_quants (WH-HL)', count(*) FROM stock_quants WHERE warehouse_id = 'a0000000-0000-0000-0000-000000000001'
UNION ALL SELECT 'stock_quants (WH-HP)', count(*) FROM stock_quants WHERE warehouse_id = 'a0000000-0000-0000-0000-000000000002'
UNION ALL SELECT 'credit_limits', count(*) FROM credit_limits
UNION ALL SELECT 'receivable_ledger', count(*) FROM receivable_ledger
UNION ALL SELECT 'sales_orders', count(*) FROM sales_orders
ORDER BY item;

-- Test credit availability cho NPP chính
SELECT '--- CREDIT AVAILABILITY ---' AS info;
SELECT c.code, c.name, 
  COALESCE(cl.credit_limit, 0) AS "hạn mức",
  COALESCE((SELECT SUM(CASE WHEN rl.ledger_type = 'debit' THEN rl.amount ELSE -rl.amount END) 
            FROM receivable_ledger rl WHERE rl.customer_id = c.id), 0) AS "dư nợ",
  COALESCE(cl.credit_limit, 0) - COALESCE((SELECT SUM(CASE WHEN rl.ledger_type = 'debit' THEN rl.amount ELSE -rl.amount END) 
            FROM receivable_ledger rl WHERE rl.customer_id = c.id), 0) AS "khả dụng"
FROM customers c
LEFT JOIN credit_limits cl ON cl.customer_id = c.id
     AND cl.effective_from <= CURRENT_DATE
     AND (cl.effective_to IS NULL OR cl.effective_to >= CURRENT_DATE)
